#include "libft.h"

int	ft_count_word(char const *s, char c)
{
	int i;
	int j;
	int f;

	f = 0;
	i = 0;
	j = 0;
	while (s[i])
	{
		if (s[i] != c && f == 0)
		{
			j++;
			f = 1;
		}
		else if (s[i] == c)
			f = 0;
		i++;
	}
	return (j);
}
